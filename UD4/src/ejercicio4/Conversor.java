package ejercicio4;
/*Crear clase Conversor con métodos:
celsiusAFahrenheit
kilometrosAMillas
*/
public class Conversor {
	
	public double celsiusAFahrenheit(double celsius) {
		return (celsius*1.8)+32;
	}
	
	
	public double kilometrosAMillas(double kilometros) {
		return kilometros*0.62137;
	}
}
